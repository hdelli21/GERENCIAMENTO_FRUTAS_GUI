// Interface que define as operações de gerenciamento de frutas
public interface GerenciamentoFrutas {
    // Método para adicionar uma fruta à lista
    void adicionarFruta(String fruta);
    
    // Método para remover uma fruta da lista
    void removerFruta(String fruta);
    
    // Método para listar todas as frutas cadastradas
    void listarFrutas();
}
